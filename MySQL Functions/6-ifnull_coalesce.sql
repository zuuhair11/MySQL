-- Display the customers full name as well as phone number, if exists if not show 'Unknown'
USE sql_store;

SELECT 
	CONCAT(first_name, ' ', last_name),
    IFNULL(phone, 'Unknown') AS phone
FROM customers;

-- OR
USE sql_store;

SELECT 
	CONCAT(first_name, ' ', last_name) AS full_name,
    COALESCE(phone, 'Unknown') AS phone
FROM customers;
