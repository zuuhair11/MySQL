SELECT NOW();                                   -- 2023-06-25 08:17:18
SELECT DATE_ADD(NOW(), INTERVAL 1 DAY);         -- 2023-06-26 08:17:18
SELECT DATE_ADD(NOW(), INTERVAL 1 YEAR);        -- 2024-06-25 08:17:18
-- ============================================================================================================
-- ============================================================================================================
SELECT DATE_ADD(NOW(), INTERVAL -1 YEAR);       -- 2022-06-25 08:17:18
-- OR
SELECT DATE_SUB(NOW(), INTERVAL 1 YEAR);        -- 2022-06-25 08:17:18
-- ============================================================================================================
-- ============================================================================================================
SELECT DATEDIFF('2023-06-25', '1997-08-02');    -- 9458      (days)
