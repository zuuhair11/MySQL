-- Those are all return an integer value 
SELECT NOW();                           -- 2023-06-25 06:57:39
-- ============================================================================================================
SELECT CURDATE();                       -- 2023-06-25
SELECT CURTIME();                       -- 06:57:39
-- ============================================================================================================
SELECT YEAR(NOW());                     -- 2023
SELECT MONTH(NOW());                    -- 6
SELECT DAY(NOW());                      -- 25
SELECT HOUR(NOW());                     -- 6
SELECT MINUTE(NOW());                   -- 57
SELECT SECOND(NOW());                   -- 39
-- ============================================================================================================
-- ============================================================================================================
-- Those are all return string value
SELECT MONTHNAME(NOW());                -- June
SELECT DAYNAME(NOW());                  -- Sunday
-- ============================================================================================================
-- ============================================================================================================
-- But is's better to use the 'EXTRACT' function, because its the standard, if u wanna change db managemnt
SELECT EXTRACT(YEAR FROM NOW());        -- 2023
SELECT EXTRACT(MONTH FROM NOW());       -- 6
SELECT EXTRACT(DAY FROM NOW());         -- 25
